function transformBlurToOpensea(blurTraits: Record<string, Record<string, any>>): Record<string, any> {
  const traits: {
    categories: Record<string, string>,

    counts: Record<string, Record<string, number>>
  } = {
    categories: {},
    counts: {}
  };

  // Initialize categories and counts based on the keys in blurTraits
  for (const category in blurTraits) {
    traits.categories[category] = "string"; // Initialize category without type
    traits.counts[category] = {};

    // Initialize each item count to 0
    for (const item in blurTraits[category]) {
      traits.counts[category][item] = 0; // Set counts to 0
    }
  }

  return traits;
}


